            440118107216220 => '岳湖村委会',
        ];
        $newAreas = [];
        foreach ($areas as $key => $name) {
            $lastThreeChars = Str::substr($key, -6); // 截取最后3位
            if ($lastThreeChars !== '000000') {//乡村/居委
                $newAreas[] = [
                    'name'      => $name,
                    'id'        => $key,
                    'parent_id' => Str::substr($key, 0, -6), // 删除最后3位
                    'level'     => 4,
                ];
            } else {
                $lastThreeChars = Str::substr($key, -9); // 截取最后6位
                if ($lastThreeChars !== '000000000') {//街道
                    $newAreas[] = [
                        'name'      => $name,
                        'id'        => Str::substr($key, 0, -6),
                        'parent_id' => Str::substr($key, 0, -9), // 删除最后6位
                        'level'     => 3,
                    ];
                } else {
                    $lastThreeChars = Str::substr($key, -11); // 截取最后8位
                    if ($lastThreeChars !== '00000000000') {//区
                        $newAreas[] = [
                            'name'      => $name,
                            'id'        => Str::substr($key, 0, -9),
                            'parent_id' => Str::substr($key, 0, -11), // 删除最后6位
                            'level'     => 2,
                        ];
                    } else {
                        $newAreas[] = [
                            'name'      => $name,
                            'id'        => Str::substr($key, 0, -11),
                            'parent_id' => Str::substr($key, 0, -13),
                            'level'     => 1,
                        ];
                    }
                }
            }
        }
        // dd(1);
        Area::insert($newAreas);
    }

    private function checkLevel($key, $name, $zeroString, $level, &$newAreas)
    {
        $lastThreeChars = Str::substr($key, -6); // 截取最后n位
        if ($lastThreeChars !== $zeroString) {
            $newAreas[] = [
                'name'      => $name,
                'id'        => $key,
                'parent_id' => Str::substr($key, 0, -6), // 删除最后n位
                'level'     => $level,
            ];
        }
    }
